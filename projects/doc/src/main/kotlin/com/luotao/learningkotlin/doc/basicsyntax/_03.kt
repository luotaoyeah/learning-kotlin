// https://kotlinlang.org/docs/basic-syntax.html#variables

package com.luotao.learningkotlin.doc.basicsyntax._03


fun main() {
    // 关键字 val 声明常量, 不能再次赋值,
    val x = 9

    // 关键字 var 声明变量, 可以重新赋值,
    var y = 6
    assert(y == 6)

    y = 9
    assert(y == 9)

    //region
    // ----------------------------------------------------------------------------------------------------

    // 支持类型推断
    val a = 9
    assert(a is Int)

    // ----------------------------------------------------------------------------------------------------
    //endregion

    //region
    // ----------------------------------------------------------------------------------------------------

    // 先声明, 再赋值, 注意这里可以用 val, 因为只赋值了一次,
    val b: Int
    b = 9
    assert(b == 9)

    // ----------------------------------------------------------------------------------------------------
    //endregion
}

//region
// ----------------------------------------------------------------------------------------------------

